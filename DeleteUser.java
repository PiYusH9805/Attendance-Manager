    private void userTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTableMouseClicked

        try {
            int dialogResult = JOptionPane.showConfirmDialog(
                    null,
                    "‚ö†Ô∏è The following will be deleted:\n"
                    + "‚Ä¢ User details\n"
                    + "‚Ä¢ Images\n"
                    + "‚Ä¢ QR codes\n"
                    + "‚Ä¢ Attendance records\n\n"
                    + "Are you sure you want to proceed?",
                    "Delete Confirmation",
                    JOptionPane.YES_NO_OPTION
            );

            if (dialogResult == JOptionPane.YES_OPTION) {

                int index = userTable.getSelectedRow();
                if (index < 0) {
                    JOptionPane.showMessageDialog(null, "‚ö†Ô∏è Please select a user first!");
                    return;
                }

                TableModel model = userTable.getModel();
                String email = model.getValueAt(index, 3).toString().trim();
                System.out.println("Deleting user with email: " + email);

                // üßπ Step 1: Delete image + QR code
                deleteFile(BDUtility.getPath("images" + File.separator + email + ".jpg"));
                deleteFile(BDUtility.getPath("qrCodes" + File.separator + email + ".jpg"));

                // üß† Step 2: Connect to MongoDB
                MongoClient mongoClient = MongoClients.create("mongodb://localhost:27017");
                MongoDatabase database = mongoClient.getDatabase("yourDatabaseName");

                // ‚úÖ Step 3: Get collections
                MongoCollection<Document> userDetails = database.getCollection("userdetails");
                MongoCollection<Document> userAttendance = database.getCollection("userattendance");

                System.out.println("‚úÖ MongoDB connected successfully!");

                // üß© Step 4: Find user by email
                Document userDoc = userDetails.find(Filters.eq("email", email)).first();

                if (userDoc != null) {
                    // Step 5: Get user ID or fallback to ObjectId
                    Object userId = userDoc.get("uniqueRegId");
                    if (userId == null && userDoc.containsKey("_id")) {
                        userId = userDoc.getObjectId("_id");
                    }

                    // Step 6: Delete attendance records linked to this user
                    DeleteResult attDel = userAttendance.deleteMany(Filters.eq("userid", userId));

                    // Step 7: Delete user document
                    DeleteResult usrDel = userDetails.deleteOne(
                            Filters.regex("email", "^" + Pattern.quote(email) + "$", "i")
                    );

                    System.out.println("üóëÔ∏è Deleted user count: " + usrDel.getDeletedCount());
                    System.out.println("üóëÔ∏è Deleted attendance count: " + attDel.getDeletedCount());

                    // Step 8: Refresh table
                    fetchUser(null);

                    JOptionPane.showMessageDialog(null,
                            "‚úÖ User deleted successfully!\nDeleted "
                            + attDel.getDeletedCount() + " attendance record(s).");
                } else {
                    JOptionPane.showMessageDialog(null, "‚ö†Ô∏è No user found with email: " + email);
                }

                mongoClient.close();

            } else {
                JOptionPane.showMessageDialog(null, "‚ùé Deletion Canceled.");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,
                    "Something went wrong: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_userTableMouseClicked

    private void fetchUser(String searchText) throws HeadlessException {
        DefaultTableModel model = (DefaultTableModel) userTable.getModel();
        model.setRowCount(0);
        try {
            // ‚úÖ MongoDB Connection (replace URI and dbName as per your setup)
            MongoClient mongoClient = MongoClients.create("mongodb://localhost:27017");
            MongoDatabase database = mongoClient.getDatabase("yourDatabaseName");
            MongoCollection<Document> collection = database.getCollection("userdetails");

            // ‚úÖ Create filter based on searchText
            Bson filter;
            if (Objects.isNull(searchText) || searchText.isEmpty()) {
                filter = new Document();  // empty filter ‚Üí all documents
            } else if (searchText.endsWith(" ")) {
                String exact = searchText.trim(); // remove space from end
                filter = Filters.or(
                        Filters.regex("name", "^" + exact + "$", "i"), // exact name match
                        Filters.regex("email", "^" + exact + "$", "i") // exact email match
                );
            } else {
                filter = Filters.or(
                        Filters.regex("name", searchText, "i"),
                        Filters.regex("email", searchText, "i")
                );

            }

            // ‚úÖ Execute query
            FindIterable<Document> resultSet = collection.find(filter);

            // ‚úÖ Iterate through results and add to model
            int idCounter =1;
            for (Document doc : resultSet) {
                model.addRow(new Object[]{
                     idCounter++,
                    doc.getString("name"),
                    doc.getString("gender"),
                    doc.getString("email"),
                    doc.getString("contact"),
                    doc.getString("address"),
                    doc.getString("state"),
                    doc.getString("country"),
                    doc.getString("uniqueregid"),
                    doc.getString("imagename")
                });
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    private void deleteFile(String filePath) {
        File fileToDelete = new File(filePath);
        if (fileToDelete.exists()) {
            if (fileToDelete.delete()) {
                System.out.println("File delete Successfully.");
            } else {
                System.out.println("Failed to delete the File.");
            }
        } else {
            System.out.println("File doesn't exist ");
        }
    }

