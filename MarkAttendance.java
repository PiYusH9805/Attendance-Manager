    private BufferedImage createCircularImage(BufferedImage image) {
        int diameter = 285;
        BufferedImage resizedImage = new BufferedImage(diameter, diameter, BufferedImage.TYPE_INT_ARGB);
        Graphics2D g2 = resizedImage.createGraphics();
        g2.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_BILINEAR);
        g2.drawImage(image, 0, 0, diameter, diameter, null);
        g2.dispose();
        BufferedImage circularImage = new BufferedImage(diameter, diameter, BufferedImage.TYPE_INT_ARGB);
        g2 = circularImage.createGraphics();
        Ellipse2D.Double circle = new Ellipse2D.Double(0, 0, diameter, diameter);
        g2.setClip(circle);
        g2.drawImage(resizedImage, 0, 0, null);
        g2.dispose();
        return circularImage;
    }

    private boolean checkInCheckOut() {

        String popUpHeader = "";
        String popUpMessage = "";
        Color color = null;

        MongoClient mongoClient = MongoClients.create("mongodb://localhost:27017");
        MongoDatabase db = mongoClient.getDatabase("yourDatabaseName");
        MongoCollection<Document> attendanceCollection = db.getCollection("userattendance");

        LocalDate currentDate = LocalDate.now();
        DateTimeFormatter dateFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String todayDate = currentDate.format(dateFormatter);

        LocalDateTime currentDateTime = LocalDateTime.now();
        DateTimeFormatter dateTimeFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");

        String userId = resultMap.get("id");     // FIX 1 â†’ Always keep userid as String

        // Find today's attendance record
        Document query = new Document("date", todayDate)
                .append("userid", userId);

        Document existingRecord = attendanceCollection.find(query).first();

        // ------------------------ CHECK-OUT ------------------------
        if (existingRecord != null) {

            String checkOutDateTime = existingRecord.getString("checkout");

            // Already checked out
            if (checkOutDateTime != null && !checkOutDateTime.isEmpty()) {

                popUpHeader = "Invalid CheckOut";
                popUpMessage = "Already checked out for the day";

                showPopUpForCertainDuration(popUpMessage, popUpHeader, JOptionPane.ERROR_MESSAGE);
                return false;
            }

            // Get check-in time
            String checkInDateTime = existingRecord.getString("checkin");
            LocalDateTime checkInLocalDateTime = LocalDateTime.parse(checkInDateTime, dateTimeFormatter);

            Duration duration = Duration.between(checkInLocalDateTime, currentDateTime);
            long hours = duration.toHours();
            long minutes = duration.minusHours(hours).toMinutes();
            long seconds = duration.minusHours(hours).minusMinutes(minutes).getSeconds();

            // Work duration too short
            if (!(hours > 0 || (hours == 0 && minutes >= 2))) {

                long remainMinutes = 1- minutes;
                long remainSeconds = 60 - seconds;

                popUpHeader = "Duration Warning";
                popUpMessage = "Your work time is less than 2 minutes.\n"
                        + "You can checkout after: " + remainMinutes + " minutes "
                        + remainSeconds + " seconds";

                showPopUpForCertainDuration(popUpMessage, popUpHeader, JOptionPane.WARNING_MESSAGE);
                return false;
            }

            // Successful Check-Out
            attendanceCollection.updateOne(
                    Filters.and(Filters.eq("userid", userId), Filters.eq("date", todayDate)),
                    Updates.combine(
                            Updates.set("checkout", currentDateTime.format(dateTimeFormatter)),
                            Updates.set("workduration", hours + " Hours and " + minutes + " Minutes")
                    )
            );

            popUpHeader = "CheckOut";
            popUpMessage = "Checked out at: " + currentDateTime.format(dateTimeFormatter)
                    + "\nWork Duration: " + hours + " Hours " + minutes + " Minutes";

            color = Color.RED;
        } // ------------------------ CHECK-IN ------------------------
        else {

            Document newAttendance = new Document()
                    .append("userid", userId)
                    .append("date", todayDate)
                    .append("checkin", currentDateTime.format(dateTimeFormatter));

            attendanceCollection.insertOne(newAttendance);

            popUpHeader = "Check In";
            popUpMessage = "Checked In at: " + currentDateTime.format(dateTimeFormatter);
            color = Color.GREEN;
        }

        lblCheckInCheckOut.setHorizontalAlignment(JLabel.CENTER);
        lblCheckInCheckOut.setText(popUpHeader);
        lblCheckInCheckOut.setForeground(color);
        lblCheckInCheckOut.setBackground(Color.DARK_GRAY);
        lblCheckInCheckOut.setOpaque(true);

        showPopUpForCertainDuration(popUpMessage, popUpHeader, JOptionPane.INFORMATION_MESSAGE);
        return true;
    }

