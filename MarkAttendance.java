    @Override
    public void run() {
        do {
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
            }

            try {
                Result result = null;
                BufferedImage image = null;

                // ---- SAFE WEBCAM FRAME READ ----
                if (webcam.isOpen()) {
                    image = webcam.getImage();
                }

                if (image == null) {
                    System.out.println("WARNING: Webcam returned NULL frame!");
                    continue;
                }

                // ---- DECODE QR ----
                LuminanceSource source = new BufferedImageLuminanceSource(image);
                BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));

                try {
                    result = new MultiFormatReader().decode(bitmap);
                } catch (NotFoundException ex) {
                    // No QR found, skip
                }

                if (result != null) {
                    String jsonString = result.getText();
                    Gson gson = new Gson();
                    java.lang.reflect.Type type = new TypeToken<Map<String, String>>() {
                    }.getType();
                    resultMap = gson.fromJson(jsonString, type);

                    String finalPath = new File("src\\Images", resultMap.get("email") + ".png").getAbsolutePath();

                    System.out.println("FINAL IMAGE PATH USED: " + finalPath);
                    CircularImageFrame(finalPath);

                }

            } catch (Exception ex) {
                ex.printStackTrace();
            }

        } while (running);
    }

